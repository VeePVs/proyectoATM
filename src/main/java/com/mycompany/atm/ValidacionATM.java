    public void rellenarNumeros(){
        int numRan, iterador;
        boolean bandera;
        for (int i = 1; i < 11; i++) {
            bandera = true;
            do {
                iterador=0;
                numRan = (int) ((Math.random() * (10 - 0)) + 0);
                for (int j = 1; j < 11; j++) {
                    if (numRan == Integer.parseInt(botones.get(j).getText())) {
                        break;
                    }
                    else{
                        iterador++;
                    }
                }
                if (iterador==10) {
                    bandera=false;
                    botones.get(i).setText(String.valueOf(numRan));
                }
            } while (bandera);
        }
    }

